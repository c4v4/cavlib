#define _CAV_SN_SWAP(i1, i2)                                   \
    do {                                                       \
        auto tmp = comp(beg[i1], beg[i2]) ? beg[i1] : beg[i2]; \
        beg[i2]  = comp(beg[i1], beg[i2]) ? beg[i2] : beg[i1]; \
        beg[i1]  = tmp;                                        \
    } while (0);

